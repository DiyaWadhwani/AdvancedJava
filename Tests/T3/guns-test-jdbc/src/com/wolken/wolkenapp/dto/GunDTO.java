package com.wolken.wolkenapp.dto;

public class GunDTO {
	
	private int gunID;
	private String gunName;
	private double gunPrice;
	private String gunSerialNum;
	private String gunBrand;
	private String gunType;
	private int gunBullets;
	private String gunCountry;
	
	
	public int getGunID() {
		return gunID;
	}
	public void setGunID(int gunID) {
		this.gunID = gunID;
	}
	public String getGunName() {
		return gunName;
	}
	public void setGunName(String gunName) {
		this.gunName = gunName;
	}
	public double getGunPrice() {
		return gunPrice;
	}
	public void setGunPrice(double gunPrice) {
		this.gunPrice = gunPrice;
	}
	public String getGunSerialNum() {
		return gunSerialNum;
	}
	public void setGunSerialNum(String gunSerialNum) {
		this.gunSerialNum = gunSerialNum;
	}
	public String getGunBrand() {
		return gunBrand;
	}
	public void setGunBrand(String gunBrand) {
		this.gunBrand = gunBrand;
	}
	public String getGunType() {
		return gunType;
	}
	public void setGunType(String gunType) {
		this.gunType = gunType;
	}
	public int getGunBullets() {
		return gunBullets;
	}
	public void setGunBullets(int gunBullets) {
		this.gunBullets = gunBullets;
	}
	public String getGunCountry() {
		return gunCountry;
	}
	public void setGunCountry(String gunCountry) {
		this.gunCountry = gunCountry;
	}
	
	

}
